    /**
     * Updates the text in the text field.
     */
    private void setUpGui(FastaParameters fastaSettings) {

        decoyTypeComboBox.setRenderer(new AlignedListCellRenderer(SwingConstants.CENTER));
        targetDecoyComboBox.setRenderer(new AlignedListCellRenderer(SwingConstants.CENTER));

        if (fastaSettings.isTargetDecoy()) {
            targetDecoyComboBox.setSelectedIndex(0);
        } else {
            targetDecoyComboBox.setSelectedIndex(1);
        }

        decoyFlagTxt.setText(fastaSettings.getDecoyFlag());

        if (fastaSettings.isDecoySuffix()) {
            decoyTypeComboBox.setSelectedIndex(1);
        } else {
            decoyTypeComboBox.setSelectedIndex(0);
        }

        fastaSuffixTxt.setText(fastaSettings.getTargetDecoyFileNameSuffix());

        targetDecoyComboBox.setEnabled(editable);
        decoyFlagTxt.setEnabled(editable);
        decoyTypeComboBox.setEnabled(editable);
        fastaSuffixTxt.setEnabled(editable);
    }

    /**
     * Indicates whether the user canceled the editing.
     *
     * @return a boolean indicating whether the user canceled the editing
     */
    public boolean isCanceled() {
        return canceled;
    }

    /**
     * Returns the FASTA settings as set by the user.
     *
     * @return the FASTA settings as set by the user
     */
    public FastaParameters getFastaSettings() {
        FastaParameters fastaSettings = new FastaParameters();
        fastaSettings.setTargetDecoy(targetDecoyComboBox.getSelectedIndex() == 0);
        fastaSettings.setDecoyFlag(decoyFlagTxt.getText());
        fastaSettings.setDecoySuffix(decoyTypeComboBox.getSelectedIndex() == 1);
        fastaSettings.setTargetDecoyFileNameSuffix(fastaSuffixTxt.getText());
        return fastaSettings;
    }

